package cn.virit.metadata.constants;

/**
 * 字段类型
 * <p>
 * 标识模型中字段的类型
 *
 * @author Virit
 * @since 1.0
 */
public enum FieldType {

    /**
     * 字符串
     */
    STRING,
    /**
     * 日期
     */
    DATE,
    /**
     * 整数
     */
    INTEGER,
    /**
     * Decimal
     */
    DECIMAL,
    /**
     * 枚举
     */
    ENUM,
    /**
     * 对象
     */
    OBJECT
}
